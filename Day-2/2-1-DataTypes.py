# Data Types

# Strings

print("Hello"[4])
print("Hello"[-1])
print("69" + "69")

print(type("69"))

# Integers
print(123 + 345)
print(123_456_789)

print(type(123_456_789))

# Floats
print(3.141597)
print(9.6)

print(type(3.141597))

# Booleans

print(True)
print(False)

print(type(True))
print(type(False))

